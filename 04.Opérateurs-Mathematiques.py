# 🧮 Opérateurs mathématiques en Python

# On crée deux variables
a = 10
b = 3

# ➕ Addition
addition = a + b
print("Addition :", addition)  # Résultat : 13

# ➖ Soustraction
soustraction = a - b
print("Soustraction :", soustraction)  # Résultat : 7

# ✖️ Multiplication
multiplication = a * b
print("Multiplication :", multiplication)  # Résultat : 30

# ➗ Division réelle (le résultat est un nombre à virgule)
division = a / b
print("Division :", division)  # Résultat : 3.333...

# 🔢 Division entière (on garde seulement la partie entière du résultat)
division_entiere = a // b
print("Division entière :", division_entiere)  # Résultat : 3

# 🧮 Modulo (le reste de la division)
modulo = a % b
print("Modulo :", modulo)  # Résultat : 1

# 💥 Puissance (exposant)
puissance = a ** b  # 10 puissance 3 = 1000
print("Puissance :", puissance)  # Résultat : 1000

# ➖ Négation (changement de signe)
negatif = -a
print("Négation :", negatif)  # Résultat : -10

# ➕ Identité (ne change rien, juste pour info)
positif = +a
print("Identité :", positif)  # Résultat : 10