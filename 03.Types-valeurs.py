# 🧠 En Python, chaque valeur a un "type"
# Le type indique ce que représente la donnée : un nombre, du texte, une valeur logique, etc.

# 🔢 1. Les nombres entiers (int = integer)
nombre_entier = 42
print(nombre_entier)  # Affiche 42
print(type(nombre_entier))  # Affiche <class 'int'>

# ➗ 2. Les nombres à virgule (float)
nombre_decimal = 3.14
print(nombre_decimal)  # Affiche 3.14
print(type(nombre_decimal))  # Affiche <class 'float'>

# 📝 3. Les chaînes de caractères (str = string)
texte = "Bonjour"
print(texte)  # Affiche Bonjour
print(type(texte))  # Affiche <class 'str'>

# 🟢 4. Les booléens (bool = True ou False)
vrai = True
faux = False
print(vrai, faux)  # Affiche True False
print(type(vrai))  # Affiche <class 'bool'>

