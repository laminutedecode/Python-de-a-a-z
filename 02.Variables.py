# 🧠 Les variables
# Une variable, c’est un nom que tu donnes à une valeur pour pouvoir la réutiliser plus tard.

# Exemple simple : on stocke un nombre dans une variable appelée "age"
age = 25

# On affiche la valeur de la variable
print(age)  # Affiche 25

# 🔁 On peut modifier la valeur d’une variable à tout moment
age = 10
print(age)  # Affiche 10
age = age + 5  # On ajoute 5 à l'âge
print(age)  # Affiche 15

# ⚠️ Attention : les noms de variables sont sensibles à la casse
Nom = "MAJUSCULE"
nom = "minuscule"
print(Nom)  # Affiche "MAJUSCULE"
print(nom)  # Affiche "minuscule" (ce sont 2 variables différentes)

